export const MP_LOCALES = Object.keys(MP_PACKS);

const EDITOR_LOCALE_STORAGE_KEY = "rpgatlas_editor_locale";

/** Best-effort current locale id (one of MP_PACKS' keys, or "en"). */
function currentLocale(): string {
  try {
    const saved = typeof localStorage !== "undefined" ? localStorage.getItem(EDITOR_LOCALE_STORAGE_KEY) : null;
    if (saved && MP_PACKS[saved]) return saved;
    const nav = typeof navigator !== "undefined" ? navigator.language : "";
    if (nav) {
      const lower = nav.toLowerCase();
      if (MP_PACKS[lower]) return lower; // e.g. "zh-tw"
      const base = lower.split("-")[0];
      if (MP_PACKS[base]) return base; // e.g. "es"
      if (base === "zh") return lower.includes("tw") || lower.includes("hant") ? "zh-tw" : "zh-cn";
    }
  } catch { /* no storage / navigator (headless) */ }
  return "en";
}

/** Translate a Beacon player-string key, substituting {placeholders}. Unknown
 *  keys fall back to English, then to the key itself — never throws. */
export function mpText(key: keyof typeof MP_EN | string, params?: Record<string, string | number>): string {
  const pack = MP_PACKS[currentLocale()];
  let s = (pack && pack[key]) ?? MP_EN[key] ?? String(key);
  if (params) for (const k of Object.keys(params)) s = s.split("{" + k + "}").join(String(params[k]));
  return s;
}
