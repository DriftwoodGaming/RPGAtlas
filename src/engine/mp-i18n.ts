/* RPGAtlas — src/engine/mp-i18n.ts
   Project Beacon MP7·D: localization for the PLAYER-facing multiplayer strings
   (the "Play Together" modal, presence toasts, friendly errors, party/battle
   feedback). The engine had no runtime i18n before — every other player string
   is English — so this is a small, self-contained table just for the Beacon
   surface, in the same ten locales the editor ships (es/fr/de/ja/zh-tw/zh-cn/
   pt/ko/it/ru), English being the identity fallback.

   Locale selection (best-effort, self-contained): the editor's saved locale
   (localStorage rpgatlas_editor_locale) wins if present, else the browser's
   `navigator.language` prefix, else English. A shipped game with no locale hint
   falls back to English — exactly the pre-MP7 behavior.

   tests-unit/mp-i18n-parity.test.ts enforces that every pack defines EXACTLY the
   English key set (no missing, no orphans) with placeholders preserved.
   GPL-3.0-or-later (see LICENSE). */

